//city class to hold information
public class City {
	public int Number;
	public String City_Code;
	public String Full_City_Name;
	public int Population;
	public int Elevation;
	public City(int num, String code, String name, int pop, int elev)
	{
		Number = num;
		City_Code = code;
		Full_City_Name = name;
		Population = pop;
		Elevation = elev;
	}

}
